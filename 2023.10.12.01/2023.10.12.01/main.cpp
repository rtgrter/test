#define _CRT_SECURE_NO_WARNINGS 1
#include <stdio.h>
#include <string.h>
#include <windows.h>
#include <math.h>

int main()
{
	int m, n, r;
	scanf("%d%d", &m, &n);
	while (r = m % n)
	{
		r = m % n;
		m = n;
		n = r;
	}
	printf("最大公约数:%d", n);
	return 0;
}

